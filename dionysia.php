   <p><h2><a name="dionysia">Dionysia?</a></h2>
   </p>
   <p><em>Poogt Poseidon je tocht van de zesde verdieping naar huis al bijna tien jaar lang te saboteren? Of ben je uiteindelijk thuis geraakt en speelt de zoete herinnering aan het Leuvens strijdgewoel je parten? Heb je de overgang naar het zomeruur slecht verwerkt? Tors je een overdreven hoeveelheid zwarte gal mee in je lichaam? Kortom: heb je de blues? </em>
   </p>
   <p><em>Doe dan zoals de ouden en zoek je heil in het theater!</em>
   </p>
   <p><em>In het jaar vijftig voor Christus werd zelfs Obelix kortstondig lid van een toneelgezelschap om zijn spitsbroeder Asterix uit diens mistroostige verbanning te verlossen. Het zal je dan ook niet verbazen dat Thiasos in de voetsporen van Aischylos, Sophocles, Euripides en Obelix treedt tijdens de jaarlijkse Dionysia.</em>
   </p>
   <p>Thiasos liet in de lente van 2008 de Dionysia herrijzen. Deze hoogdagen zijn geen exacte replica van hun antieke voorganger, maar bieden een gemoderniseerde versie van het oude theaterfestival. Amateurtheatergroepen krijgen de kans om een eigen stuk, gelieerd aan de Oudheid, voor een publiek van liefhebbers te presenteren.</p>
   <p>Sinds de tweede editie (2009) slaan Thiasos en de Klassieke Kring Gent de handen in elkaar, en organiseren beurtelings de Dionysia.</p>
   <p>Dit jaar bezoeken Dionysus en Apollo Leuven. Vijf groepen treden aan en dingen mee naar de hoogst mogelijke eer. De kelen worden stilaan gesmeerd, decorverf wordt in emmers aangesleept en de souffleur oefent zijn zachtste fluistertoon.</p>
   <p>Maar wie mag met de klimoptak en de prijsgeit naar huis?</p>

