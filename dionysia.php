   <p><strong>Aphrodite</strong> Sten Van Gorp</p>
   <p><strong>Artemis</strong> Sten Van Gorp</p>
   <p><strong>Bode</strong> Maxime Maleux</p>
   <p><strong>Jachtvrienden</strong> Andy Peetermans, Matthias Strauven</p>
