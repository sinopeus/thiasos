   <p><strong><h3>1. Zonder Matthew (JONNA)</h3> </strong></p>


   <p>Matthew had er niet beter op gevonden om op reis te gaan .</p>
   <p>Ons geloof kreeg een knauw, waar zijn de de goden gebleven, onze bescherming , bestaan ze wel echt?</p>
   <p>Die vervloekte mannen, wat denken ze wel dat ze alles voor het zeggen hebben.</p>
   <p>Weg met ze , vooruit met de geit, we maken onze eigen wereld los van mannen en goden.</p>
   <p>Weg van de bestaande normen en waarden.</p>
   <p><strong>Regie:</strong> Pol Pauwels</p>
   <p><strong>Acteurs:</strong> Coraline Kusters, Marthe Raeymaekers, Tida Weemeeuw, Remco Revalk</p>
   
   <p><strong><h3>2. Phaedra (Studenten Grieks/Latijn KU Leuven)</h3> </strong></p>
   <p><strong>Synopsis</strong>: Niemand moet ooit wagen om Aphrodite te weerstreven; wie het toch doet, zal daar haar straf voor ondergaan. En haar woede keert zich nu tegen Theseus’ zoon, Hippolytus! Maar wat met het werktuig van Aphrodite’s wraak op Hippolytus? Wanneer haar liefde en haar eer tegenover elkaar komen te staan, moet Phaedra alles doen wat ze maar kan om haar geheim te bewaren. Tot ze in een hoek gedreven wordt.</p>
   <p>Een bewerking van Euripides en Seneca waarin Phaedra’s tweestrijd en onschuld de aandacht krijgen.</p>
   <p><strong>Tekst en regie:</strong>
   Simon Van Roost</p>
