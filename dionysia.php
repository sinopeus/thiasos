   <p><strong><h3>3. Bloot en Bloed (JONNA)</h3></strong></p>
   <p>JONNA vzw uit Wilsele biedt tijdens het schooljaar toneellessen aan voor jongeren vanaf 9 jaar.</p>
   <p>Om de groepen van mekaar te onderscheiden krijgen ze geen nummer maar kiezen ze zelf een ludieke naam. De Kapstokken brengen een bewerking van De Baccanten onder de titel “Bloot en bloed”. Met een beperkte bezetting proberen we het verhaal te spelen van Dionysos die met zijn volgelingen naar Thebe komt om zich daar als god te manifesteren. Maar hij komt vooral om zijn moeder Semele te wreken. Wanneer koning Pentheus hem hardhandig wil aanpakken zal hij deze wraak op geraffineerde manier ten uitvoer brengen. Agave, de moeder van Pentheus, zal in een roes haar zoon voor een roofdier aanzien en doden. Dan komt ze tot inkeer en beseft ze wat ze gedaan heeft.</p>
   <p><strong>Begeleiding:</strong> Hendrik Van Eycken</p>
   <p><strong>Spelers:</strong> Valerie Paredis, Marie Peeters, Eva Simon, Bas Van Hoeck, Piter Hillewaert, Milo Podolski, Julie Behaegel, Jelle Debille </p>
   <p><strong><span style="font-variant: small-caps;">Woensdag 29 april</span></strong>
   </p>
   <p><strong><h3>1. Een veldslag om een hart (Academie MWD Gooik)</h3></strong></p>
   <p>Helena, koningin en mooiste vrouw van Griekenland, heeft haar man Menelaos en dochter Hermione achtergelaten om haar nieuwe liefde Alexandros te volgen naar Ilion, beter bekend als Troje. Menelaos overtuigt alle koningen en helden van Griekenland om ten strijde te trekken en zijn vrouw terug te halen. Wat voorgesteld wordt als een avontuurlijk uitstapje, een kwestie van een paar weken vooraleer ze terug zullen keren met schepen bol van zilver en goud, draait heel anders uit. Agamemnon wordt verplicht zijn dochter te offeren, om wind af te dwingen voor de honderden zeilschepen. En aan Ilion wordt niet minder dan tien jaar lang een bittere strijd gevoerd met grote verliezen aan beide zijden en zonder dat deze beslist wordt. Tot op een dag de Griekse kampen er volledig verlaten bijliggen en er enkel een groot houten paard voor de poort staat ...<br />In zijn reeks van 'Kleine klassiekers' zet Michael de Cock deze keer zijn tanden in de Ilias van Homeros en maakt er een verteerbare tekst van. Hij kiest daarbij voor het standpunt van Helena zelf, wat natuurlijk toelaat haar gevoelens bij dit alles aan bod te laten komen. Het wordt voorgesteld alsof zij haar verhaal vertelt aan haar dochter Hermione, al raakt dat hier en daar toch wel ondergesneeuwd onder de gebeurtenissen</p>
   <p><strong>Acteurs:</strong> Kaisa Vannimmen, Lore Bonte, Joke Billens ,Kato Vankelecom, Marie Claes, Joran Parent, Sofie Cerstelotte, Fien Breynaert, Remke Van Cauwelaert, Nina Van Praet, Anja Peeters</p>
   <p><strong>Regie/ bewerking tekst:</strong> Sophie Verbeke</p>
   <p><strong>Choreografie:</strong> Els Duponchelle</p>
   <p><strong><h3>2. Tongen? (Klassieke Kring UGent)</h3></strong></p>
   <p>Twee zusjes en één echtgenoot, meer heeft een goed verhaal vaak niet
   nodig. In een eigen moderne bewerking brengt de KK dit jaar een verhaal dat
   ook Ovidius inspireerde voor zijn Metamorphosen. Een klassiek verhaal over
   hoe de ideale echtgenoot Tereus, eer en macht(smisbruik) de twee zussen
   Procne en Philomela ver uit elkaar drijft, maar uiteindelijk ook weer
   samenbrengt. Liefde in haar vele vormen of gewoon your everyday
   driehoeksverhouding, kortom een verhaal dat nog steeds brandend actueel is
   aangekruid met een flinke portie muziek en dans.</p>
   <p><strong>Regie:</strong> Oettie Devriese</p>
   <p><strong>Choreo:</strong> Silke Bomberna</p>
   <p><strong>Spel:</strong> Matilde De Cooman, Marie Jacobs, Tijl De Bruyn</p>
   <p><strong>Tekst:</strong> Lisa Verleyen, Silke Bomberna</p>
   <p><strong>Zang:</strong> Mieke D'hondt, Elisabeth De Backer</p>
   <p><strong>Piano:</strong> Laura Versyp</p>

   
