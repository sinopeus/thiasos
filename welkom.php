<h2>Welkom</h2>
  <p class="">Thiasos richt jaarlijks een ruimte in waarin op
verschillende manieren aandacht wordt besteed aan klassieke
cultuur. Onder "klassieke cultuur" verstaan we niet alleen het antieke
Griekenland en Rome, maar ook het moderne Griekenland en Italië en de receptie van de
antieke klassieke cultuur. Thiasos is ook een ontmoetingsplek voor
studenten die Grieks of Latijn studeren of in klassieke cultuur zijn
geïnteresseerd, en in een onderlinge dialoog op een
niet-studiegebonden wijze de klassieke cultuur willen exploreren. Het
bestuur van Thiasos wordt gevormd vanuit een samenwerkingsverband
tussen doctorandi, studenten en oud-studenten van de KU Leuven.</p>
